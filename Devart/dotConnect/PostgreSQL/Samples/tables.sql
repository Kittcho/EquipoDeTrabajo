CREATE FUNCTION Dept_Insert(IN INT,IN VARCHAR(14),IN VARCHAR(13))
RETURNS VOID AS $$ 
INSERT INTO Dept(DeptNo, DName, Loc) VALUES($1, $2, $3); 
$$ LANGUAGE SQL;

CREATE FUNCTION Dept_Update(IN INT,IN VARCHAR(14),IN VARCHAR(13))
RETURNS VOID AS $$ 
UPDATE Dept SET DName = $2, Loc = $3 WHERE DeptNo = $1;
$$ LANGUAGE SQL;

CREATE OR REPLACE FUNCTION note_dept()
            RETURNS TRIGGER AS $$
            BEGIN
            NOTIFY "table_changed_test.DEPT";
            RETURN NULL;
            END;
          $$ LANGUAGE plpgsql;

CREATE OR REPLACE FUNCTION note_emp()
            RETURNS TRIGGER AS $$
            BEGIN
            NOTIFY "table_changed_test.emp";
            RETURN NULL;
            END;
          $$ LANGUAGE plpgsql;

CREATE TRIGGER notify_dept AFTER INSERT OR UPDATE OR DELETE
            ON DEPT
            EXECUTE PROCEDURE note_dept();

CREATE TRIGGER notify_emp AFTER INSERT OR UPDATE OR DELETE
            ON emp
            EXECUTE PROCEDURE note_emp();

